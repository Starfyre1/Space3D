package com.starfyre1.space3D.core.entity;

public class Model {
	/*****************************************************************************
	 * Constants
	 ****************************************************************************/

	/*****************************************************************************
	 * Member Variables
	 ****************************************************************************/
	private int	mId;
	private int	mVertexCount;

	/*****************************************************************************
	 * Constructors
	 ****************************************************************************/
	public Model(int id, int vertexCount) {
		mId = id;
		mVertexCount = vertexCount;
	}

	/*****************************************************************************
	 * Methods
	 ****************************************************************************/

	/*****************************************************************************
	 * Setter's and Getter's
	 ****************************************************************************/
	/** @return The id. */
	public int getId() {
		return mId;
	}

	/** @return The vertexCount. */
	public int getVertexCount() {
		return mVertexCount;
	}

	/*****************************************************************************
	 * Serialization
	 ****************************************************************************/
}
